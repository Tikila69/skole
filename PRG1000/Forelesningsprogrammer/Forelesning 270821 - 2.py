#matematiske operatorer

#mulplikasjon, *
produkt=8*7
print('produktet er',produkt)

#divisjon, /
resultat=76/4
print('resultatet av 76/4 er',resultat)

#heltallsdivisjon, //
resultat=77/4
heltallsdivisjon=77//4
print('resultatet av 77/4 er',resultat)
print('resultatet av 77//4 er',heltallsdivisjon)

#restdivisjon, %
resultat=77/4
restdivisjon=77%4
print('resultatet av 77/4 er',resultat)
print('restdivisjonen av 77%4 er',restdivisjon)
